
#include<iostream>

using namespace std;

int main()
{
	//  Relational / Comparison Operator Program

int a = 15;
int b = 89;

cout<<"The bool value of Equality (==) is "<<(a==b)<<endl;
cout<<"The bool value of Unquality (!=) is "<<(a!=b)<<endl;
cout<<"The bool value of Lesser than (<) is "<<(a<b)<<endl;
cout<<"The bool value of Greater than (>) is "<<(a>b)<<endl;
cout<<"The bool value of Greater than equal to (==) is "<<(a>=b)<<endl;
cout<<"The bool value of Lesser than equal to (<=) is "<<(a<=b)<<endl;

return 0;
}